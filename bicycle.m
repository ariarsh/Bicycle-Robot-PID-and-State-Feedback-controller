function dy=bicycle(y,u)
d2=1;
c1=1;
c2=-1;
c3=1;
d1=0.1;
%dy=zeros(4,1);
dy(1,1)=y(2);
dy(2,1)=( 0.5*d2*c1*sin(2*y(3)) + c2*sin(y(3)*y(4)*y(4)) +c3*u  ) / (  1-d1*c1*cos(y(3))*cos(y(3))  );
dy(3,1)=y(4);
dy(4,1)=( 0.5*d1*c2*sin(2*y(3))*y(4)*y(4) + d1*c3*cos(y(3))*u + d2*sin(y(3))  )/ ( 1-d1*c1*cos(y(3))*cos(y(3)) );
